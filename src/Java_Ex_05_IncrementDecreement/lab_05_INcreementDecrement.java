package Java_Ex_05_IncrementDecreement;

public class lab_05_INcreementDecrement {
    public static void main(String[] args) {
        int a = 10;
        int b= ++a;
        System.out.println(b);// increement and assign a->10+1=11
        System.out.println(a);//assigned the latest value of a i.e 11
    }
}
